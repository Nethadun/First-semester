    private void getAllCustomer() {
        try {
            dtm.setRowCount(0);
            ArrayList<CustomerDTO> allCustomer=CustomerController.getAllCustomer();
            for (CustomerDTO customer : allCustomer) {
                Object[] rowData={customer.getCust_Id(),customer.getCust_Name(),customer.getCust_Address(),customer.getCust_NIC(),customer.getCust_Driv_Lic(),customer.getCust_Mobile_Num()};
                dtm.addRow(rowData);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
