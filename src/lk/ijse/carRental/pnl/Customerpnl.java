    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String custId=txtcustid.getText();
            String custName=txtcustname.getText();
            String custAddress=txtcustaddress.getText();
            String custNIC=txtcustnic.getText();
            String custDrILic=txtcustdrivindlic.getText();
            int custMobile=Integer.parseInt(txtmobilenum.getText());
            
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            Object rowData[]={custId,custName,custAddress,custNIC,custDrILic,custMobile};
            model.addRow(rowData);
            
            CustomerDTO cust=new CustomerDTO(custId, custName, custAddress, custNIC, custDrILic, custMobile);
            boolean b=CustomerController.addCustomer(cust);
            if(b){
                clearAll();
                getAllCustomer();
                JOptionPane.showMessageDialog(this, "ADD...");
            }else{
                JOptionPane.showMessageDialog(this, "FAILD...");
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

