    private void txtcustidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcustidActionPerformed
        try {
            String custId=txtcustid.getText();
            CustomerDTO searchCustomer=CustomerController.searchCustomer(custId);
            txtcustid.setText(searchCustomer.getCust_Id());
            txtcustname.setText(searchCustomer.getCust_Name());
            txtcustaddress.setText(searchCustomer.getCust_Address());
            txtcustnic.setText(searchCustomer.getCust_NIC());
            txtcustdrivindlic.setText(searchCustomer.getCust_Driv_Lic());
            txtmobilenum.setText(searchCustomer.getCust_Mobile_Num()+ "");
            txtcustname.requestFocus();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Customerpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtcustidActionPerformed

