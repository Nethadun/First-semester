    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String driverId = did.getText();
            String driverName = dname.getText();
            String driverLisence = dlisence.getText();
            int driverAge = Integer.parseInt(dage.getText());
            String driverAddress = daddress.getText();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] rowData = {driverId, driverName, driverLisence, driverAge, driverAddress};
            model.addRow(rowData);

            DriverDTO driver = new DriverDTO(driverId, driverName, driverLisence, driverAge, driverAddress);
            boolean a = DriverController.addDriver(driver);
            if (a) {
                getAllDriver();
                clearAll();
                JOptionPane.showMessageDialog(this, "ADD..");
            } else {
                JOptionPane.showMessageDialog(this, "FAILD..");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

