    private void didActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_didActionPerformed
        try {
            String driverId = did.getText();
            DriverDTO searchDriver = DriverController.searchDriver(driverId);
            did.setText(searchDriver.getDriver_Id());
            dname.setText(searchDriver.getDriver_Name());
            dlisence.setText(searchDriver.getDriver_Licence());
            dage.setText(searchDriver.getDriver_Age() + "");
            daddress.setText(searchDriver.getDriver_Address());
            dname.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_didActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String driverId = did.getText();
            String driverName = dname.getText();
            String driverLisence = dlisence.getText();
            int driverAge = Integer.parseInt(dage.getText());
            String driverAddress = daddress.getText();

            DriverDTO update = new DriverDTO(driverId, driverName, driverLisence, driverAge, driverAddress);
            boolean u = DriverController.updateDriver(update);

            if (u) {
                clearAll();
                getAllDriver();
                JOptionPane.showMessageDialog(this, "UPDATE..");
            } else {
                JOptionPane.showMessageDialog(this, "FAILD..");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Driverpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

