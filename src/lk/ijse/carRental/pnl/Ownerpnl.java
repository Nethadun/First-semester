    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String ownerId = ownerid.getText();
            String ownerName = ownername.getText();
            String ownerAdd = owneradd.getText();
            String ownerNic = ownernic.getText();
            int ownerMob = Integer.parseInt(ownermob.getText());

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] rowData = {ownerId, ownerName, ownerAdd, ownerNic, ownerMob};
            model.addRow(rowData);

            OwnerDTO owner = new OwnerDTO(ownerId, ownerName, ownerAdd, ownerNic, ownerMob);
            boolean add = OwnerController.addOwner(owner);

            if (add) {
                JOptionPane.showMessageDialog(this, "ADD...");
            } else {
                JOptionPane.showMessageDialog(this, "FAILD..");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ownerpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Ownerpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

