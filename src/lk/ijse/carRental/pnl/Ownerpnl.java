    private void getAllOwner() {
        try {
            dtm.setRowCount(0);
            ArrayList<OwnerDTO> allOwner = OwnerController.getAllOwner();
            for (OwnerDTO owner : allOwner) {
                Object[] rowData = {owner.getOwner_Id(), owner.getOwner_Name(), owner.getOwner_Address(), owner.getOwner_NIC(), owner.getOwner_Mob()};
                dtm.addRow(rowData);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ownerpnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Ownerpnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

