    private void vehiIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vehiIdActionPerformed
        try {
            String vehicleId=vehiId.getText();
            VehicleDTO searchVehicle=VehicleController.searchVehicle(vehicleId);
            cobowner.setSelectedItem(searchVehicle.getOwner_Id());
            comtype.setSelectedItem(searchVehicle.getVehicle_Type_Id());
            colour.setText(searchVehicle.getColour());
            rent.setText(searchVehicle.getRent()+"");
            pass.setText(searchVehicle.getNo_Pass()+"");
            cobowner.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_vehiIdActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String vehicleId = vehiId.getText();
            String ownerID = cobowner.getSelectedItem().toString();
            String vehicleType = comtype.getSelectedItem().toString();
            String colur = colour.getText();
            double rentD = Double.parseDouble(this.rent.getText());
            int noOfPass = Integer.parseInt(pass.getText());

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] rowData = {vehicleId, vehicleType, colur, rentD, noOfPass};
            model.addRow(rowData);

            VehicleDTO vehicle = new VehicleDTO(vehicleId, ownerID, vehicleType, colur, rentD, noOfPass);
            boolean b = VehicleController.addVehicle(vehicle);

            if (b) {
                JOptionPane.showMessageDialog(this, "ADD..");
            } else {
                JOptionPane.showMessageDialog(this, "FAILD..");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

