    private void loardAllOwner() {
        try {
            cobowner.removeAll();
            ArrayList<OwnerDTO> allOwner = OwnerController.getAllOwner();
            for (OwnerDTO ownerDTO : allOwner) {
                cobowner.addItem(ownerDTO.getOwner_Id());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getAllVehicle() {
        try {
            dtm.setRowCount(0);
            ArrayList<VehicleDTO> allVehicle = VehicleController.getAllVehicle();
            for (VehicleDTO vehicle : allVehicle) {
                Object[] rowData = {vehicle.getVehicle_Id(), vehicle.getVehicle_Type_Id(), vehicle.getColour(), vehicle.getRent(), vehicle.getNo_Pass()};
                dtm.addRow(rowData);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loardAllVehicleTypes() {
        try {
            comtype.removeAll();
            ArrayList<VehicleTypeDTO> allvehicleType = VehicleTypeController.getAllVehicleType();
            for (VehicleTypeDTO vehicleTypeDTO : allvehicleType) {
                comtype.addItem(vehicleTypeDTO.getVehicle_Type_Id());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Vehiclepnl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

